import seededRandom from './seededRandom'

const fetchAPI = function(date: Date): string[] {
    let result: string[] = [];
    let random = seededRandom(date.getDate());

    for(let i = 17; i <= 23; i++) {
        if(random() < 0.5) {
            result.push(i + ':00');
        }
        if(random() < 0.5) {
            result.push(i + ':30');
        }
    }
    return result;
}

export default fetchAPI